public class IncDec{
	public static void main(String x[]){
		int a=10, b;
		System.out.println("Postincrement");
		b=a++;
		System.out.println("A="+a+" "+"B="+b);
		System.out.println("-----------------------------------------------------------------------------------------------");
		System.out.println("Preincrement");
		b=++a;
		System.out.println("A="+a+" "+"B="+b);		
		
	}
}

